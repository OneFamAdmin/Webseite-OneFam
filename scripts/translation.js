// Function to change language
function changeLanguage(lang) {
  document.getElementById(
    "intro"
  ).innerHTML = `<h1>${translations[lang].intro}</h1>
		<video autoplay loop muted playsinline>
			<source src="images/2.mp4" type="video/mp4">
		</video>`;

  // Update vision section
  document.getElementById("vision-title").textContent =
    translations[lang].visionTitle;
  document.getElementById("vision-text").textContent =
    translations[lang].visionText;

  // Update Code of Conduct section
  document.querySelector(".textBlock-heading").textContent =
    translations[lang].codeConductTitle;
  document.querySelector(".textBlock p").textContent =
    translations[lang].codeConductText;

  // Update Accordion Section
  const accordionItems = [
    "loveRespect",
    "openCommunication",
    "supportUnity",
    "sharedTime",
    "responsibility",
    "diversityAcceptance",
    "educationDevelopment",
    "gratitude",
    "conflictResolution",
    "safetySecurity",
    "communityService",
  ];

  accordionItems.forEach((item, index) => {
    document.querySelector(
      `.accordionItem:nth-child(${index + 1}) .accordionItem-heading span`
    ).textContent = translations[lang][`${item}Title`];

    document.querySelector(
      `.accordionItem:nth-child(${index + 1}) .accordionItem-body p`
    ).textContent = translations[lang][`${item}Text`];
  });

  // Update culture section
  document.getElementById("culture-title").textContent =
    translations[lang].cultureTitle;
  document.getElementById("culture-text").textContent =
    translations[lang].cultureText;

  // Update philosophy section
  document.getElementById("philosophy-title").textContent =
    translations[lang].philosophyTitle;
  document.getElementById("philosophy-text").textContent =
    translations[lang].philosophyText;

  // Update social media section
  document.querySelector(".social-media-text").textContent =
    translations[lang].socialMediaText;
  document.querySelector(".footer-content p:nth-child(1)").textContent =
    translations[lang].impressum;
  document.querySelector(".footer-content p:nth-child(2)").textContent =
    translations[lang].copyRight;

  // Store language preference
  localStorage.setItem("selectedLanguage", lang);
}

// Apply saved language on page load
document.addEventListener("DOMContentLoaded", function () {
  const savedLang = localStorage.getItem("selectedLanguage") || "en";
  changeLanguage(savedLang);
});

// Event listener for flag clicks
document.querySelectorAll(".flag").forEach((flag) => {
  flag.addEventListener("click", function () {
    const selectedLang = this.getAttribute("data-lang");
    changeLanguage(selectedLang);
  });
});
